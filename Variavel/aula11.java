package variaveis;
/*
- Aula 11: Operadores Aritmeticos - Parte 1
    - Adição (+);
    - Subtração (-);
    - Multiplicação (*);
    - Divisão (/);
    - Módulo (%): Resto da divisão;
*/
public class aula11 {
    public static void main(String[] args) {
        
        int soma = 12 + 5;
        System.out.println(soma);

        int subtracao = 12 - 5;
        System.out.println(subtracao);

        int multiplicacao = 12 * 5;
        System.out.println(multiplicacao);

        double divisao = 12 / 5;
        System.out.println(divisao);

        double modulo = 12 % 5;
        System.out.println(modulo);
    }
}
