package variaveis;
/*
- Aula 7: Char
    - Char é um tipo de dado primitivo que armazena um unico caractere
    - O char pode armazenar qualquer caractere Unicode, incluindo letras, numeros e simbolos
    - Ocupa 2 bytes de memoria, permitindo armazenar 65.536 caracteres diferentes;
    - Char so aceita aspas simples, se colocar dupla o codigo vai achar que é uma string e vai da erro

*/
public class aula7 {
    public static void main(String[] args) {
		
		char letra = 'A';
		
		System.out.println(letra);
		
		String letra2 = "A";
		
		System.out.println(letra2);
		
		char simbolo = '$';
		
		System.out.println(simbolo);
	}
}
