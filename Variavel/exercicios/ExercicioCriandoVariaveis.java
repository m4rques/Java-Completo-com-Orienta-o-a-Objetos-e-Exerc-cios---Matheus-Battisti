package variaveis.exercicio;
/*
    Criando variáveis

    Neste exercício, você praticará a criação e atribuição de variáveis em Java. 
    Seu objetivo é definir quatro variáveis de diferentes tipos e atribuir valores a elas. 
    As variáveis que você deverá criar são:

    Uma variável inteira (int) chamada idade, que deve receber o valor 25.
    Uma variável de texto (String) chamada nome, que deve receber o valor "João".
    Uma variável booleana (boolean) chamada isEstudante, que deve receber o valor true.
    Uma variável de ponto flutuante (double) chamada altura, que deve receber o valor 1.75.
*/
public class ExercicioCriandoVariaveis {
    public static void main(String[] args) {
        
        int idade = 25;
        String nome = "João";
        boolean isEstudante = true;
        double altura = 1.75;
        
    }
}
